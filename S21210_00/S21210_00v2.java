package s21210_00;

public class S21210_00v2 {

	public static void main(String[] args) {
		int evenSum = 0;
		int oddSum = 0;
		for(int cnt = 1; cnt < 11; cnt++) {
			if (cnt % 2 == 0)
			{
				evenSum += cnt;
			}
			else
			{
				oddSum += cnt;
			}
			System.out.println("cnt:" + cnt + " oddSum: " + oddSum + " evenSum: " + evenSum + " allSum " + (oddSum + evenSum));
		}
		
	}

}
